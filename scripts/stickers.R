library(hexSticker)


# euler
imgurl <- "img/org/euler.webp"
hexSticker::sticker(imgurl, package = "", 
                    p_color =  "blue",
                    p_y = 1.6,
                    h_fill = "black",
                    h_color = "blue",
                    s_x = 1.0,
                    s_y = 1.0,
                    s_width = 1,
                    url = "  euler",
                    u_color = "blue",
                    u_size = 12,
                    u_x = 0.95,
                    u_y = 0.11,
                    white_around_sticker = TRUE,
                    filename = "img/euler.png")




